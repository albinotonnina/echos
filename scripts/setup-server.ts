#!/usr/bin/env -S pnpm tsx

/**
 * EchOS Web Setup Server
 *
 * Zero-dependency (uses only Node.js built-ins) server that serves a
 * browser-based setup wizard. Writes .env and creates data directories.
 *
 * Usage:
 *   pnpm wizard              # starts server + opens browser
 *   pnpm wizard --port 3456  # custom port
 */

import * as fs from 'node:fs';
import * as path from 'node:path';
import * as http from 'node:http';
import { randomBytes } from 'node:crypto';
import { createConnection } from 'node:net';
import { exec, execSync } from 'node:child_process';

const args = process.argv.slice(2);

// Detect Homebrew install by checking if echos-daemon wrapper exists in PATH
const IS_BREW_INSTALL = (() => {
  try {
    execSync('which echos-daemon', { stdio: 'ignore' });
    return true;
  } catch {
    return false;
  }
})();

// Resolve the Homebrew prefix so we can read/write the canonical env path
// (e.g. /opt/homebrew/etc/echos/.env) and default data dirs to var/echos/*.
const BREW_PREFIX = (() => {
  if (!IS_BREW_INSTALL) return '';
  try {
    return execSync('brew --prefix', { encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
})();

/** Return the canonical .env file path for the current install type. */
function getEnvPath(): string {
  if (IS_BREW_INSTALL && BREW_PREFIX) {
    return path.join(BREW_PREFIX, 'etc', 'echos', '.env');
  }
  return path.resolve('.env');
}

/** Return default data directory for a given subdir (knowledge, db, sessions). */
function defaultDataDir(subdir: string): string {
  if (IS_BREW_INSTALL && BREW_PREFIX) {
    return path.join(BREW_PREFIX, 'var', 'echos', subdir);
  }
  return `./data/${subdir}`;
}
const PORT = (() => {
  const portArgIndex = args.indexOf('--port');
  if (portArgIndex === -1) return 3456;

  const rawPort = args[portArgIndex + 1];
  if (!rawPort || rawPort.startsWith('-')) {
    console.error('Error: --port flag requires a numeric value.\nUsage: pnpm wizard --port 3456');
    process.exit(1);
  }

  const parsedPort = parseInt(rawPort, 10);
  if (!Number.isInteger(parsedPort) || parsedPort <= 0 || parsedPort > 65535) {
    console.error(`Error: Invalid port "${rawPort}". Port must be an integer between 1 and 65535.`);
    process.exit(1);
  }

  return parsedPort;
})();

// ─── Helpers ──────────────────────────────────────────────────────────────────

function parseEnvFile(content: string): Record<string, string> {
  const result: Record<string, string> = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    const raw = trimmed.slice(eq + 1).trim();
    const value = raw.replace(/^(['"])(.*)\1$/, '$2');
    result[key] = value;
  }
  return result;
}

function sanitizeEnvValue(value: string): string {
  // Strip newlines/carriage returns to prevent env injection
  return value.replace(/[\r\n]/g, '');
}

/** Quote env values that contain special chars to prevent silent truncation */
function quoteEnvValue(value: string): string {
  if (!value) return '';
  const sanitized = sanitizeEnvValue(value);
  if (/[\s#'"\\]/.test(sanitized)) {
    return `"${sanitized.replace(/["\\]/g, '\\$&')}"`;
  }
  return sanitized;
}

